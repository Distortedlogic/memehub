import { Field, ObjectType } from '@nestjs/graphql';
import { Column, Entity } from 'typeorm';
import { NodeColumns } from '../abstracts/node.columns';
import { EPromptCategory } from './enums/EPromptCategory';

@ObjectType()
@Entity('prompts')
export class PromptEntity extends NodeColumns {
  @Field()
  @Column()
  name: string;

  @Field(() => EPromptCategory)
  @Column({ type: 'enum', enum: EPromptCategory })
  category: EPromptCategory;

  @Field()
  @Column()
  description: string;

  @Field()
  @Column()
  value: string;

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
