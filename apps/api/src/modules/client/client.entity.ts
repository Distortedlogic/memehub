import { Field, ObjectType } from '@nestjs/graphql';
import { Column, Entity } from 'typeorm';
import { NodeColumns } from '../abstracts/node.columns';

@ObjectType()
@Entity('clients')
export class ClientEntity extends NodeColumns {
  @Field()
  @Column()
  name: string;

  @Field()
  @Column({ length: 1_000 })
  description: string;

  @Field()
  @Column({ length: 1_000 })
  objective: string;

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  @Field()
  @Column('uuid', { name: 'firm_id' })
  firmId: string;

  @Field()
  @Column('uuid', { name: 'workspace_id' })
  workspaceId: string;
}
