import { Field, ObjectType } from '@nestjs/graphql';
import { Column, Entity } from 'typeorm';
import { NodeColumns } from '../../abstracts/node.columns';

@ObjectType()
@Entity('firms')
export class FirmEntity extends NodeColumns {
  @Field()
  @Column()
  name: string;

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  @Field({ nullable: true })
  @Column('uuid', { name: 'avatar_id', nullable: true })
  avatarId?: string;

  @Field()
  @Column('uuid', { name: 'address_id' })
  addressId: string;
}
