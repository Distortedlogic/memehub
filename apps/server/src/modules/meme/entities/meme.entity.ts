import { Field, Int, ObjectType } from '@nestjs/graphql';
import { Column, Entity } from 'typeorm';
import { VotableColumns } from '../../common/columns/votable';

@ObjectType()
@Entity('memes')
export class MemeEntity extends VotableColumns {
  @Field()
  @Column()
  title: string;

  @Field(() => Int)
  @Column({ default: 0, name: 'num_comments' })
  numComments: number;

  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  @Field()
  @Column('uuid', { name: 'image_id' })
  imageId: string;
}
